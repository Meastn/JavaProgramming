package day7_Operators;

public class BooleanOperators {
    public static void main(String[] args) {

        boolean result1 = 20 <= 40;
        System.out.println("result1 = " + result1);

        result1 = 20 >= 40;
        System.out.println("result1 = " + result1);

        // to receive a loan you have to have credit score of 720

        int score = 345;
        boolean loanResult = score >= 720;

        System.out.println("loanResult = " + loanResult);

        System.out.println("--------------");

       boolean result7 = 10 == 10;
        System.out.println("result7 = " + result7);

        boolean result9 = "Muhtar" == "Good Guy";
        System.out.println("result9 = " + result9);

    }
}

package day03_EscapeSequences;
/*
Escape sequences are used only within double brackets.
\n newline
\t one tab forward for the text
\\ write a backslash
\" write a double-quote

 */
public class EscapeSequences {

    public static void main(String[] args) {
        System.out.println("Hello Cydeo \n \t How are you all today \n \t\t It's nice to see you all! \n \t\t\t What class do we have next?");
        System.out.println("I'm writing a \"double quote\" here! ");
        System.out.println("And a real \\ (backslash) here");
        System.out.println("----------- \n-----------");
    }

}

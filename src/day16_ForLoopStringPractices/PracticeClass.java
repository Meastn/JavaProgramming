package day16_ForLoopStringPractices;

public class PracticeClass {
    public static void main(String[] args) {

        for (int i = 0; i <=100 ; i++) {
            if (i%10==0) {
                System.out.println(i + ",");
            } else {
                System.out.println(i);
            }
        }
    }
}
